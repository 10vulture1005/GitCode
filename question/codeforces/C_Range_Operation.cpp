int answer(vector<int> &a, vector<int> &w, int idx, int wt, vector<vector<int>> &dp)
{

     int t=0;
    if (dp[idx][wt] != -1)
        return dp[idx][wt];

    if(wt<0)return -100;
    if (idx == w.size() - 1)
    {
        if (w[w.size() - 1] <= wt)
            return a[idx];
        else
            return 0;
    }
    int nt = answer(a, w, idx + 1, wt, dp);
    if(wt>=w[idx])
     t = a[idx] + answer(a, w, idx + 1, wt - w[idx], dp);
    dp[idx][wt] = max(t, nt);

    return max(t, nt);
}

void vulture()
{
    int n, x;
    cin >> n >> x;
    vector<int> a(n);
    vin(a, a.size());
    vector<vector<int>> dp(1000, vector<int>(1000,-1));
    vector<int> w(n);
    vin(w, a.size());
    int ans = answer(a, w, 0, x, dp);
    cout << ans << endl;
}
