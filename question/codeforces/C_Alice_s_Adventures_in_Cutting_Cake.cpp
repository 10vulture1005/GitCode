void vulture(){
    int n,m,v;
    cin>>n>>m>>v;
    vector<int> a(n);
    vin(a,a.size());
    int sumt = accumulate(a.begin(),a.end(),0LL);

    int lo = 0, hi = sumt;
    if(sumt<m*v){
        cout<<-1<<endl;return;
    }
    
    auto check = [&](int mid) -> bool {
            int n = a.size();

    if (mid == 0) {
        int sum = 0;
        int cnt = 0;
        for (int i = 0; i < n; i++) {
            sum += a[i];
            if (sum >= v) {
                cnt++;
                sum = 0;
            }
        }
        return cnt >= m;
    }

    vector<int> left(n+1, 0), right(n+2, 0);

    int sum = 0;
    for (int i = 0; i < n; i++) {
        left[i+1] = left[i];
        sum += a[i];
        if (sum >= v) {
            left[i+1]++;
            sum = 0;
        }
    }

    sum = 0;
    for (int i = n-1; i >= 0; i--) {
        right[i] = right[i+1];
        sum += a[i];
        if (sum >= v) {
            right[i]++;
            sum = 0;
        }
    }

    int cur = 0;
    int l = 0;

    for (int r = 0; r < n; r++) {
        cur += a[r];

        while (cur >= mid && l <= r) {
            if (left[l] + right[r+1] >= m)
                return true;

            cur -= a[l];
            l++;
        }
    }

    return false;
    };
    
    int ans = -1;
    while (lo <= hi) {
        int mid = lo + (hi - lo) / 2;
    
        if (check(mid)) {
            ans = mid;
            lo = mid + 1;   // go bigger
        } else {
            hi = mid - 1;
        }
    }
    
    cout << ans << "\n";
}
