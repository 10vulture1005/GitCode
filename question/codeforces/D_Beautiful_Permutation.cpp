void vulture(){
int n;
cin>>n;
int sp,sa;

cout<<1<<' '<<1<<' '<<n<<endl;
cin>>sp;
cout<<2<<' '<<1<<' '<<n<<endl;
cin>>sa;
int len = sa-sp;
if(len==n){
    cout<<'!'<<' '<<1<<' '<<n<<endl;
    return;

}
int s =1;
int e = n;  
int l,r;
while(s<=e){
    int mid = s+(e-s)/2;
    int p,a;
    cout<<1<<' '<<s<<' '<<mid<<endl;
    cin>>p;
    cout<<2<<' '<<s<<' '<<mid<<endl;
    cin>>a;
    int diff = a-p;
    if(diff==0){
        s=mid+1;
    }else if(diff==len){
         l=s;
        r = mid;
        e = mid;
        if(e-s+1==len)break;
       
    }else{
        int rr = len-diff;
        l = mid+1-diff;
        r = mid+rr;
        break;
    }

}


cout<<'!'<<' '<<l<<' '<<r<<endl;


}
