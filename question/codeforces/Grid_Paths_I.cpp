int MOD = 1e9+7;
void vulture()
{
    int n;
    cin >> n;
    vector<string> a(n);
    vin(a, a.size());

    vector<vector<int>> dp(n, vector<int>(n));
    if (a[0][0] == '*')
    {
        cout << 0 << endl;
        return;
    }
    dp[0][0] = 1;
    for (int j = 1; j < n; j++)
    {
        if (a[0][j] == '*')
        {
            continue;
        }
        dp[0][j] =(dp[0][j]%MOD +dp[0][j - 1]%MOD)%MOD;
    }
    for (int i = 1; i < n; i++)
    {
        if (a[i][0] == '*')
        {
            continue;
        }
        dp[i][0] =(dp[i][0]%MOD+ dp[i - 1][0]%MOD)%MOD;
    }
    for (int i = 1; i < n; i++)
    {
        for(int j = 1; j < n; j++) {
            if(a[i][j]=='*'){
                continue;
            }
            dp[i][j]+=(dp[i-1][j]%MOD+dp[i][j-1]%MOD)%MOD;
        }
    }

    cout<<dp[n-1][n-1]<<endl;
}
signed main()
{
    input_tej_le;
    int t=1;
    // cin >> t;
    while (t--)
    {
        vulture();
    }
}