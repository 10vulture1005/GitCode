/* ================= SOLVE ================= */
void vulture() {
    int n;
    cin>>n;
    vector<int> a(n+1);
    for(int i = 1; i <= n; i++) {
        cin>>a[i];
    }

    for(int i = 1; i <= n; i++) {
        a[i]^=a[i-1];
    }

    fre[0]=1;
    int ans = 0;
    for(int i = 1; i <= n; i++) {
        for(auto it:sq){
            ans-=fre[it^a[i]];
        }

        fre[a[i]]++;

        
    }
    ans+=n*(n+1)/2;

    for(int i = 0; i <= n; i++) {
        fre[a[i]]=0;
    }
    cout<<ans<<endl;

}



