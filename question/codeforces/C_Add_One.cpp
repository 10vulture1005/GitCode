int mod = 1e9+7;

vector<vector<int>> dp(2e5+1,vector<int>(10,-1));

int rec(int m, int num){

    if(m==0){
        return 1;
    }
    if(dp[m][num]!=-1){
        return dp[m][num];
    }

    if(10-num<=m){
        int mm=m-(10-num);
        
        dp[m][num]=(rec(mm,0)%mod+rec(mm,1)%mod)%mod;
        return dp[m][num];

        
    }
    else{
        dp[m][num]=1;
        return 1;
    }
}
void vulture(){
    
    int n, m;
    cin>>n>>m;
    int ans = 0;

    while(n){
        ans=((rec(m,(n)%10))%mod+ans%mod)%mod;
        n/=10;
    }
    
    cout<<ans<<endl;
}
