void vulture(){
 int n;
cin >> n;

vector<int> a(n + 1);
for (int i = 1; i <= n; i++) {cin >> a[i];}
vector<int> dp(n + 1, 0);
map<int, deque<int>> m;

for (int i = 1; i <= n; i++) {
    dp[i] = dp[i - 1];

    m[a[i]].push_back(i);

    if (m[a[i]].size() > a[i])
        m[a[i]].pop_front();

    if (m[a[i]].size() == a[i]) {
        
        dp[i] = max(dp[i], dp[m[a[i]].front() - 1] + a[i]);
    }
}

cout<<dp[n]<<endl;
}
