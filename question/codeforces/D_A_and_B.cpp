int cost(vector<int> &v){

    int m = v.size();
    if (m == 0) return 0;   

    int mid;

    mid = v[m / 2]-m/2;


    int ans = 0;
    for (int i = 0; i < m; i++) {
        ans += abs((v[i] - (i)) - mid);
    }
    return ans;
}
void vulture()
{
    int n;
    string s;
    cin >> n >> s;
    vector<int> a;
    // a.pb(0);
    vector<int> b;
    // b.pb(0);
    for (int i = 0; i < n; i++)
    {
        if (s[i] == 'a')
            a.pb(i + 1);
        else
            b.pb(i + 1);
    }

 
    int c1 = cost(a);
    int ans = cost(b);
    ans = min(ans, c1);
    cout << ans << endl;
}
