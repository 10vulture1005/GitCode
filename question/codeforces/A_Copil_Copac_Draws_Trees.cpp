vector<bool> visited;
vector<vector<int>> graph;

void dfs(int p,int vertex,vector<pair<int,int>> &dp,map<pair<int,int> ,int> &m){

if(visited[vertex]){
    return;
}

if(m[{p,vertex}]!=0)
dp[vertex].first = m[{p,vertex}];
else{
    dp[vertex].first = m[{vertex,p}];
}
visited[vertex]=1;
// cout<<vertex<<endl;
    for(auto it:graph[vertex]){
    dfs(vertex,it,dp,m);
    }


}


void dfsdp(int p,int vertex,vector<pair<int,int>> &dp,map<pair<int,int> ,int> &m){

if(visited[vertex]){
    return;
}
// cout<<p<<' '<<vertex<<endl;
if(dp[p].first<=dp[vertex].first){
    dp[vertex].second=dp[p].second;
}else{
    dp[vertex].second=dp[p].second+1;
    // cout<<p<<endl;
}
visited[vertex]=1;
// cout<<vertex<<endl;
    for(auto it:graph[vertex]){
    dfsdp(vertex,it,dp,m);
    }


}


