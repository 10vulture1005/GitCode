void vulture(){
int n,q;
cin>>n>>q;
string s;
cin>>s;

vector<int> a(q);
vin(a,a.size());

int ct = count(s.begin(),s.end(),'B');

if(ct==0){
    for(int i = 0; i <q;i++){
    cout<<a[i]<<endl;
    }
    return;
}
vector<int> ans;
for(int i = 0; i < q; i++) {
    int an =0;
    while(a[i]){
        if(s[an%s.size()]=='A'){
            a[i]--;
        }else{
            a[i]/=2;
        }
        an++;
    }
    ans.pb(an);

}

for(int i = 0; i <ans.size();i++){
cout<<ans[i]<<endl;
}



}
