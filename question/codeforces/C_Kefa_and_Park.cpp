vector<vector<int>> g(1e5);
vector<bool> visited(1e5);

int ans = 0;

void dfs(int vertex,int parent, int ct,vector<int>&cat,int m){
if(cat[vertex-1]){
    ct++;
    // cout<<ct<<endl;
}else{
    ct=0;
}
if(ct>m){
    return;
}
// if(g[vertex].size()==0){
//     ans++;
//     cout<<ans<<endl;
// }


bool isleaf=1;
// cout<<vertex<<endl;
    for(auto it:g[vertex]){
        if(it==parent)continue;
        isleaf = 0;
    dfs(it,vertex,ct,cat,m);
    }

    if(isleaf){
        if(vertex==1){
            if(g[vertex].empty()) ans++;
        }else{
            ans++;
        }
    }


}



