void vulture(){
    int n;
    cin>>n;
    vector<int> a(n);
    vin(a,a.size());
    vector<int> b(n);
    vin(b,a.size());
    int ans = 1;
    int mod = 1e9+7;
    sort all(a);
    sort all(b);
    int ct = 0;
    for(int i = n-1; i >=0; i--) {
        auto it = lower_bound(a.begin(),a.end(),b[i]);
        if(it!=a.end() and *it!=b[i]){
            it--;
        }else if(it==a.end()){
            cout<<0<<endl;
            return;
        }
         auto it2 = it-a.begin();

        int ele = n-it2-1;
        
        ans = ((ans%mod)*((ele-ct)%mod))%mod;
        ct++;
        // cout<<ele<<endl;
    }
    cout<<ans<<endl;

}
