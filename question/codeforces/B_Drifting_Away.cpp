void vulture(){
    string s;
    cin>>s;
    int n = s.size();
    for(int i = 0; i < n; i++) {
        if(s[i]=='*' and s[i+1]=='<'){
            cout<<-1<<endl;return;
        }
        if(s[i]=='>' and s[i+1]=='*'){
            cout<<-1<<endl;return;

        }if(s[i]=='*' and s[i+1]=='*'){
            cout<<-1<<endl;return;

        }if(s[i]=='>' and s[i+1]=='<'){
            cout<<-1<<endl;return;

        }
    }

    int ct = count(s.begin(),s.end(),'>');
    int ct2 = count(s.begin(),s.end(),'<');
    if(ct+ct2==n){
        cout<<max(ct,ct2)<<endl;
    }else{
        cout<<max(ct,ct2)+1<<endl;
    }
}
