void vulture()
{
    int n, k;
    cin >> n >> k;

    int lo = k, hi = k + n - 1;

    auto check = [&](int mid)
    {
        int p = mid * (mid + 1) / 2 - (k - 1) * k / 2;
        int s = (k + n - 1) * (k + n) / 2 - (k - 1) * k / 2;
        return 2 * p <= s;
    };

    int ans = k;

    while (lo <= hi)
    {
        int mid = lo + (hi - lo) / 2;

        if (check(mid))
        {
            ans = mid;
            lo = mid + 1;
        }
        else
        {
            hi = mid - 1;
        }
    }

    int res = 1e9;
    int s1 = (k + n - 1) * (k + n) / 2 - (k - 1) * k / 2;

    int p1 = ans * (ans + 1) / 2 - (k - 1) * k / 2;
    res = (abs(2 * p1 - s1));

    if (ans + 1 >= k && ans + 1 <= k + n - 1)
    {
        int tmp = (ans + 1) * (ans + 2) / 2 - (k - 1) * k / 2;
        res = min(res, abs(2 * tmp - s1));
    }

    cout << res << "\n";
}
