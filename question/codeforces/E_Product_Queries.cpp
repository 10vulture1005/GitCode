vector<vector<int>> fac(300001);
void vulture(){
    int n;
    cin >> n;

    set<int> s;
    for(int i = 0; i < n; i++){
        int x;
        cin >> x;
        s.insert(x);
    }

    vector<int> dp(n + 1, -1);

    for(int i = 1; i <= n; i++){
        if(s.find(i)!=s.end()){
            dp[i] = 1;
        }
    }

    for(int i = 1; i <= n; i++){
        for(int j = 0; j < fac[i].size(); j++) {

            if(dp[fac[i][j]] != -1 and dp[i/fac[i][j]] != -1){
                int ma = dp[fac[i][j]] + dp[i/fac[i][j]];
                if(dp[i] == -1 or ma < dp[i]){
                    dp[i] = ma;
                }
            }
        }
    }

    for(int i = 1; i <= n; i++){
        cout << dp[i] << " ";
    }
    cout << endl;
}

