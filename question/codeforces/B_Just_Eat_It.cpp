vector<int> findMaxSubarray(const std::vector<int>& nums) {
    vector<int> result(3);
    result[0] = -1e9; 
    result[1] = -1;
    result[2] = -1;

    long long currentSum = 0;
    int currentStart = 0;

    for (int i = 0; i < nums.size(); ++i) {
        currentSum += nums[i];

        if (currentSum > result[0]) {
            result[0]= currentSum;
            result[1] = currentStart;
            result[2] = i;
        }

        if (currentSum < 0) {
            currentSum = 0;
            currentStart = i + 1; 
        }
    }

    if (result[0] == -1e9) {
        long long maxNegative = -1e9;
        int maxNegativeIndex = -1;
        for (int i = 0; i < nums.size(); ++i) {
            if (nums[i] > maxNegative) {
                maxNegative = nums[i];
                maxNegativeIndex = i;
            }
        }
        result[0] = maxNegative;
        result[1] = maxNegativeIndex;
        result[2] = maxNegativeIndex;
    }

    return result;
}

