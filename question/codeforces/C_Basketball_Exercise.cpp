void vulture(){
    int n;
    cin>>n;
    vector<int> a(n);
    vector<int> b(n);
    vin(a,a.size());
    vin(b,a.size());
    vector<vector<int>> dp(n+1,vector<int>(3));
    for(int i = 1; i <= n; i++) {
        // till i what is max;
        dp[i][0] = max({dp[i-1][0],dp[i-1][1],dp[i-1][2]});
        dp[i][1] = max({dp[i-1][0]+a[i-1],dp[i-1][2]+a[i-1]});
        dp[i][2] = max({dp[i-1][0]+b[i-1],dp[i-1][1]+b[i-1]});

    }
   
    cout<<max({dp[n][0],dp[n][1],dp[n][2]})<<endl;
}
