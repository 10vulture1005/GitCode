vector<vector<int>> graph;
vector<bool> visited;
vector<int> ans;
vector<int> con;
int ct = 0;

void dfs(int node) {
    visited[node] = true;
    ct++;
    con.push_back(node);

    for (int neighbor : graph[node]) {
        if (!visited[neighbor]) {
            dfs(neighbor);
        }
    }
}

void vulture() {
    int n, m;
    cin >> n >> m;

    graph.resize(n+1);
    visited.resize(n+1, false);
    ans.resize(n+1, 0);

    for (int i = 0; i < m; i++) {
        int k;
        cin >> k;


        vector<int> grp(k);
        for (int j = 0; j < k; j++) {
            cin >> grp[j];
        }

        for (int j = 1; j < k; j++) {
            graph[grp[0]].push_back(grp[j]);
            graph[grp[j]].push_back(grp[0]);
        }
    }

    for (int i = 1; i <= n; i++) {
        if (!visited[i]) {
            ct = 0;
            con.clear();

            dfs(i);   

            for (int node : con) {
                ans[node] = (con.size());
            }
        }
    }

    for (int i = 1; i <= n; i++) {
        cout << ans[i] << " ";
    }
    cout << endl;
}

