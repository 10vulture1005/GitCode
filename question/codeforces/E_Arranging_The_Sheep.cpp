void vulture(){
int n;
cin>>n;
string a;
cin>>a;

int ct = count(a.begin(),a.end(), '*');

int avg = 0;

for(int i = 0; i < n; i++) {
    if(a[i]=='*'){
        avg+=(i+1);
    }
}
if(ct!=0)
avg/=ct;
else{
    cout<<0<<endl;return;
}

vector<int> p;
p.pb(0);
for(int i = 0; i < n; i++) {
    if(a[i]=='*'){
        p.pb(i+1);
    }
}
int c = 0;
if(ct%2==0){
avg = (p[ct/2]+p[ct/2+1])/2;
}else{
    avg = p[ct/2+1];
}
vector<int> pos(ct+1,avg);
// cout<<ct/2<<endl;
for(int i = (ct+1)/2; i < pos.size(); i++) {
    pos[i]+=c++;
}
c=1;
for(int i = (ct+1)/2-1; i >0; i--) {
   pos[i]-=c++; 
}
int ans = 0;
for(int i = 1; i < pos.size(); i++) {
    ans+=abs(pos[i]-p[i]);
    // cout<<abs(pos[i])<<' '<<p[i]<<endl;
}
cout<<ans<<endl;
// cout<<endl;

}
