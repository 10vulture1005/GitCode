void vulture(){
    int n,m;
    cin>>n>>m;
    vector<int> a(m);
    vin(a,a.size());

    long long lo = 0, hi = 2*m;
    
    auto check = [&](long long t) -> bool {
        vector<int> fre(n+1);
        for(int i = 0; i < m; i++) {
            fre[a[i]]++;

        }
        int tm = 0;
        for(int i = 1; i < n+1; i++) {
            if(t>=fre[i]){
                tm+=(t-fre[i])/2;
            }else{
                tm-=fre[i]-t;
            }
        }
        return tm>=0;
    };
    
    long long ans = 1;
    while (lo <= hi) {
        long long mid = lo + (hi - lo) / 2;
    
        if (!check(mid)) {
            lo = mid + 1;   
        } else {
            ans = mid;

            hi = mid - 1;
        }
    }
    
    cout << ans << "\n";


}
