int tsp(int n,map<pair<int,set<int>>,int> &dp,set<int> &r, vector<vector<int>> &a){

    if(r.size()==0){
        return a[n][0];
    }
    auto key = make_pair(n, r);
    auto itdp = dp.find(key);
    if(itdp != dp.end()) return itdp->second;

    int ans = 10000;
    
    for(auto it:r){
set<int> tr = r;
    tr.erase(it);
        ans = min(ans,a[n][it]+ tsp(it,dp,tr,a));
    }        
    dp[{n,r}] = ans;

    return ans;

}

