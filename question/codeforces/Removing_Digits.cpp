void vulture(){
int n;
cin>>n;
vector<int> dp(n+1,1e9);
dp[0]=0;
for(int i = 1; i <=n; i++) {
    vector<int> dig;
    int tmi = i;
    while(tmi!=0){
        dig.pb(tmi%10);
        tmi/=10;
    }


    for(auto it:dig){
        if(i-it>=0){
            dp[i] = min(dp[i],dp[i-it]+1);
        }
    }
}
cout<<dp[n]<<endl;
}
