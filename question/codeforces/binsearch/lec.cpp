bool check(int t,int k,vector<int> &a) {
    int n = a.size();
    int tk=k;
    int sum = 0;
    for(int i = 0; i < n; i++) {
        if(tk==0 or a[i]>t){
            return false;
        }
        if(sum+a[i]>t){
            tk--;
            sum=a[i];
        }else{
            sum+=a[i];
        }
    }
    if(tk==0){
        return 0;
    }
    
    return true;

}

void vulture(){
    int n,k;
    cin>>n>>k;
    vector<int> a(n);
    vin(a,a.size());
    int s = 0;
    
    int e = 1e16;
    int ans = -1;
    while (s <= e) {
        long long mid = s + (e - s) / 2;

        if (check(mid,k,a)) {            
            ans = mid;       
            e = mid - 1;    
        } else {
            s = mid + 1;    
        }
    }
    cout<<ans<<endl;
}
