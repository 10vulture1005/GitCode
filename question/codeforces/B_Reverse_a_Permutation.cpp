    int idx = -1;
    for(int i = 0; i < n; i++) {
        if(a[i]==num){
            num--;
        }else{
            idx = i;
            
            break;

        }
    }
    
    if(num==0){
        for(int i = 0; i <n;i++){
        cout<<a[i]<<" ";
        }
        cout<<endl;return;
    }
    for(int i = 0; i < n; i++) {
        if(a[i] ==num){
            num = i;
            break;
        }
    }
    reverse(a.begin()+idx,a.begin()+num+1);
    for(int i = 0; i <n;i++){
    cout<<a[i]<<" ";
    }
