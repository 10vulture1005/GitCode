void vulture(){
    int n,q;
    cin>>n>>q;
    vector<int> a(n);
    vin(a,a.size());
    vector<int> b(n);
    vin(b,a.size());


    vector<int> ans(n);
    for(int i = n-1; i >=0; i--) {
        if(i==n-1){
            ans[i] = max(a[i],b[i]);
            continue;
        }
        ans[i] = max({a[i],b[i],ans[i+1]});
    }
    // for(int i = 0; i <n;i++){
    // cout<<ans[i]<<" ";
    // }
    // cout<<endl;

    for(int i = 1; i < n; i++) {
        ans[i] +=ans[i-1];
    }



    while(q--){
        int l,r;
        cin>>l>>r;

        l--;
        r--;

        if(l==0){
            cout<<ans[r]<<' ';
            continue;
        }
        cout<<ans[r]-ans[l-1]<<' ';

    }
    cout<<endl;


}
