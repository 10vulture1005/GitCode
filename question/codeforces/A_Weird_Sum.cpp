void vulture(){
    int n,m;
    cin>>n>>m;
    vector<vector<int>> a(n, vector<int>(m));
    
    int mm = -1;
    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            cin >> a[i][j];
            mm = max(a[i][j],mm);
        }
    }
    vector<int> fre(mm+1);
    vector<vector<int>> x(mm+1);
    vector<vector<int>> y(mm+1);
    for(int i = 0;i<n;i++){
    for(int j = 0;j<m;j++){
        fre[a[i][j]]++;
        x[a[i][j]].pb(i+1);
        y[a[i][j]].pb(j+1);

    }
    }
    int ans = 0;
    for(int i = 1; i <= mm; i++) {
        sort all(x[i]);
        sort all(y[i]);
    }
    for(int i = 1; i <= mm; i++) {
        if(fre[i]){
            for(int j = 0; j < x[i].size(); j++) {
                ans+=x[i][j]*(x[i].size()-2*j-1);
            }for(int j = 0; j < y[i].size(); j++) {
                ans+=y[i][j]*(y[i].size()-2*j-1);
            }
        }
    }
    cout<<-ans<<endl;






    


}
