void vulture(){
int n;
cin>>n;
string s;
cin>>s;
int ran = n+1;
int ct = count(s.begin(),s.end(),'a');
int ctb = n-ct;
if(ct==ctb){
    cout<<0<<endl;return;
}
map<int,int> m;
vector<int> a(n);
for(int i = 0; i < n; i++) {
    if(s[i]=='a'){
        a[i] = 1;
    }else{
        a[i] = -1;
    }
}
int c = 0;
m[0];
for(int i = 1; i <= n; i++) {
    c+= a[i-1];
    if(m.find(c-(ct-ctb))!=m.end()){
        auto it = m.find(c-(ct-ctb));
        if(ran>i-it->second){
            // cout<<i<<" "<<it->second<<endl;
            ran = min(ran,i-it->second);
        }
    }m[c]=i;


}
if(ran==n){
    cout<<-1<<endl;return;
}
cout<<ran<<endl;

}
