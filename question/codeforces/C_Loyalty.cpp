void vulture(){
int n,x;
cin>>n>>x;
vector<int> a(n);
vin(a,a.size());

sort all(a);
int s = 0;

int re=x;

vector<int> ans;
int pts= 0;
int l=0,r=n-1;
while(l<=r){
    
        if((s+a[r])==s){
        ans.pb(a[l]);
        s+=a[l++];
        
        }
    else{
        pts+=a[r];
        ans.pb(a[r]);
        re = (re / x + 1) * x;

        s+=a[r--];

    }

}
cout<<pts<<endl;
for(int i = 0; i <ans.size();i++){
cout<<ans[i]<<" ";
}
cout<<endl;


}
