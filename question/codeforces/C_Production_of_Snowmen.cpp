void vulture()
{
    int n;
    cin >> n;

    vector<int> a(n), b(n), c(n);
    vin(a, n);
    vin(b, n);
    vin(c, n);
    vector<int> tb = b;

    sort(tb.begin(), tb.end());
    vector<int> tm;

    for (int x : tb)
    {
        if (tm.empty() || tm.back() != x)
        {
            tm.push_back(x);
        }
    }
    tb = tm;

    map<int, vector<bool>> lo;
    map<int, vector<bool>> hi;

    for (int v : tb)
    {
        vector<bool> s(n), e(n);
        for (int i = 0; i < n; i++)
        {
            s[i] = (a[i] < v);
            e[i] = (c[i] > v);
        }
        lo[v] = s;
        hi[v] = e;
    }

    vector<bool> cklo(n, 1);
    vector<bool> ckhi(n, 1);

    for (int i = 0; i < n; i++)
    {
        auto l = lo[b[i]];
        auto h = hi[b[i]];
        for (int j = 0; j < n; j++)
        {
            if (cklo[j])
                cklo[j] = l[(j + i) % n];
            if (ckhi[j])
                ckhi[j] = h[(j + i) % n];
        }
    }

    int ctl = 0, cth = 0;
    for (int i = 0; i < n; i++)
    {
        ctl += cklo[i];
        cth += ckhi[i];
    }

    cout << n * cth * ctl << endl;
}

signed main()
{
    input_tej_le;
    int t;
    cin >> t;
    while (t--)
    {
        vulture();
    }
}