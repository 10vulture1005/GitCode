void vulture(){
    int n;
    cin>>n;
    vector<pair<int,int>> seg(n);
    for(int i = 0; i < n; i++) {
        int l,r;cin>>l>>r;
        seg[i] = {l,r};
    }

    long long lo = 0, hi = 1e9;
    
    auto check = [&](long long k) -> bool {
        pair<int,int> pos = {0,0};
        for (int i = 0; i < n; i++) {
        pos.first -= k;
        pos.second += k;

        pos.first = max(pos.first, seg[i].first);
        pos.second = min(pos.second, seg[i].second);

        if (pos.first > pos.second) return 0;
    }
    return 1;
        
    };
    
    long long ans = -1;
    while (lo <= hi) {
        long long mid = lo + (hi - lo) / 2;
        // cout<<mid<<endl;
        if (!check(mid)) {
            lo = mid + 1;   // go bigger
        } else {
                        ans = mid;

            hi = mid - 1;
        }
    }
    
    cout << ans << "\n";
}
