void vulture(){
int n;
cin>>n;
string s;
cin>>s;
map<int,int> m;

vector<int> p(n);
p[0] = s[0]-'0';
for(int i = 1; i < n; i++) {
    p[i] = p[i-1]+(s[i]-'0');
}
// for(int i = 0; i <n;i++){
// cout<<p[i]<<" ";
// }
// cout<<endl;

int ans = 0;
for(int i = 0; i < n; i++) {
    if(p[i]==i+1){
        ans++;
    }
    m[p[i]-(i+1)]++;
}

// for(auto it:m){
// cout<<it.first<<' '<<it.second<<endl;
// }

for(auto it:m){
ans+=(it.second*(it.second-1))/2;
}
cout<<ans<<endl;

}
