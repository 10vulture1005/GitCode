void vulture(){
int n;
cin>>n;

vector<int> a(n+2);
a[0]=1;
for(int i = 1; i <= n; i++) {
    for(int j = 1; j <= 6; j++) {
        if(i-j<0){
            continue;
        }else{
            a[i] = (a[i]%MOD+a[i-j]%MOD)%MOD;
        }
    }
}
cout<<a[n]%MOD<<endl;

}
