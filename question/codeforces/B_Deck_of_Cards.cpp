void vulture(){
int n,k;
cin>>n>>k;
string s;
cin>>s;
if(n==1 and k==1){
    cout<<'-'<<endl;return;
}
int ct = count(s.begin(),s.end(),'0');
int ct1 = count(s.begin(),s.end(),'1');
int ct2 = count(s.begin(),s.end(),'2');
string ans = "";
for(int i = 0; i < n; i++) {
    ans+="-";
}
int lt = max(ct,k-ct1-ct2);
int rt = min(ct+ct2,k-ct1);

for(int i = 1; i <= n; i++) {
    int l = i-n+k;
    int r = i-1;
    if(max(lt,l)<=min(rt,r)){

        if(rt+1<=n-k+lt and i>=rt+1 and i<=n-k+lt){
            ans[i-1]='+';
        }else{
            ans[i-1]='?';
        }
    }else{
        ans[i-1]='-';
    }
    
    
}
cout<<ans<<endl;

}
