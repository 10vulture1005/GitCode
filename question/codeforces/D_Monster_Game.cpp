void vulture(){
      int n;
    cin >> n;

    vector<int> a(n);
    vector<int> b(n);
    vin(a,a.size());
    vin(b,a.size());

    

    sort(a.begin(), a.end());

    
    vector<int> pre(n + 1, 0);
    for (int i = 1; i <= n; i++)
        pre[i] = pre[i-1] + b[i-1];

    int ans = 0;

    for (int i = 0; i < n; i++) {
        

        int swrd = n - i;

        int lo = 0, hi = n, k = 0;
        while (lo <= hi) {
            int mid = lo + (hi - lo) / 2;
            if (pre[mid] <= swrd) {
                k = mid;
                lo = mid + 1;
            } else {
                hi = mid - 1;
            }
        }

        ans = max(ans, a[i] * k);
    }

    cout<<ans<<endl;
}
