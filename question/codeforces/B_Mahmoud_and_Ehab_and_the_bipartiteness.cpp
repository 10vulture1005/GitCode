vector<pair<int,vector<int>>> g(1e5+5,{-1,{}});
void dfs(int p,int x){
    // cout<<x<<endl;

    for(auto it:g[x].second){
        if(it==p){
            continue;
        }
        g[it].first=-g[x].first;
        dfs(x,it);
    }

}

void vulture(){
int n;
cin>>n;
for(int i = 0; i < n-1; i++) {
    int s,e;
    cin>>s>>e;
    g[s].second.pb(e);
    g[e].second.pb(s);
}
dfs(0,1);
int nct = 0;
int ct = 0;
for(int i = 1; i <=n; i++) {
    if(g[i].first==-1)nct++;
    else ct++;
}

int ans = 0;
for(int i = 1; i <=n; i++) {
    if(g[i].first==1){
        ans+=nct-g[i].second.size();
    }
}
cout<<ans<<endl;


}
