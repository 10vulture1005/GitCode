vector<vector<int>> graph(1e5);
vector<int> ans;
bool found =0;
int Ks;
void dfs(int vertex, int parent) {
    visited[vertex] = 1;
    ans.pb(vertex);

    for(auto it: graph[vertex]) {
        if(it == parent) continue;  
        if(visited[it]) {
            if(ans.size() > Ks) {
                found = 1;
                cout << ans.size() << endl;
                for(int x: ans) cout << x << " ";
                cout << endl;
                return;
            }
        } else {
            dfs(it, vertex);
            if(found) return;
        }
    }

    ans.pop_back();  
}

