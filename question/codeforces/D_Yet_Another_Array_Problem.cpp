vector<int> sieve(int n) {
    
    // creation of boolean array
    vector<bool> prime(n + 1, true);
    for (int p = 2; p * p <= n; p++) {
        if (prime[p] == true) {
            
            // marking as false
            for (int i = p * p; i <= n; i += p)
                prime[i] = false;
        }
    }
    
    vector<int> res;
    for (int p = 2; p <= n; p++){
        if (prime[p]){ 
            res.push_back(p);
        }
    }
    return res;
}

void vulture(){
int n;
cin>>n;
vector<int> a(n);
vin(a,a.size());
int ans = 1e18;
vector<int> prime = sieve(100);


for(int i = 0; i < n; i++) {
    for(auto it:prime){
        if(a[i]%it!=0){
            ans = min(ans,it);
            break;
        }
    }
}
cout<<ans<<endl;



}
