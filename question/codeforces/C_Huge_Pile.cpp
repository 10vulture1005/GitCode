void vulture(){
    int n,k;
    cin>>n>>k;
    if(n==k){
        cout<<0<<endl;return;
    }
    if(n<k){
        cout<<-1<<endl;return;
    }


    vector<int> a;
    a.pb(n);
    int ans = 0;

    while(a.size()!=0){
   
        ans++;
        set<int> div;
        for(int i = 0; i < a.size(); i++) {
            int x = a[i]/2;
            int y = ((a[i]+1)/2);
            if(x==k or y==k){
                cout<<ans<<endl;return;
            }
            else{
                if(x>k){
                    div.insert(x);
                }if(y>k)div.insert(y);
            }
        }
        vector<int> tmp;
        

        
        if(div.size()==0){
            cout<<-1<<endl;return;
        }

                
        for(auto it:div){
            tmp.pb(it);
        }
        a=tmp;



        
    }



}
