void vulture(){
    int n,k;
    cin>>n>>k;
    vector<long double> a(n);
    vin(a,a.size());
    long double ts = 0.0L;
    for(int i = 0; i < k; i++) {
        ts+=a[i];
    }
    long double ans = 0.0L;
    long double din = (long double)(n-k+1);

    for(int i = 0; i <= n-k; i++) {
        if(i>0 ) ts=ts-a[i-1]+a[i+k-1];
        ans+=(ts/din);
    }

    
    cout<<fixed<<setprecision(10)<<ans<<endl;
}
