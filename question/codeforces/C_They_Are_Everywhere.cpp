void minRange(string arr, int n, int k)
{
   
    int start = 0, end = n;

    unordered_map<char, int> map;

 
    int i = 0, j = 0;

    while (j < n) {
        map[arr[j]]++;
        j++;

        if (map.size() < k)
            continue;

   

        while (map.size() == k) {
            int windowLen = (j - 1) - i + 1;
            int subArrayLen = end - start + 1;

            if (subArrayLen > windowLen) {
                start = i;
                end = j - 1;
            }


            if (map[arr[i]] == 1)
                map.erase(arr[i]);

            else
                map[arr[i]]--;

            i++;
        }
    }

    if (start == 0 && end == n)
        cout << -1 << endl;

    else
        cout << end-start+1 << endl;
}


