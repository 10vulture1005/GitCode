void vulture(){
    int n,a,b,c;
    cin>>n>>a>>b>>c;
    vector<int> d = {a,b,c};
    vector<int> dp(4001);
    dp[0]=0;
    dp[a]=1;
    dp[b]=1;
    dp[c]=1;
    for(int i = 1;i<=n;i++){
    for(auto it:d){
        if(i-it>0 and dp[i-it]!=0){
            dp[i] = max(dp[i],dp[i-it]+1);
        }
    
    }
    }
    
    cout<<dp[n]<<endl;
}
