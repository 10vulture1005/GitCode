void vulture(){
    string s;
    cin>>s;
    int ans = 0;
    int n=s.size();
    for(int i = 0; i < n; i++) {
        if((i==0 or i==n-1) and s[i]=='u'){
            ans++;
            s[i]='s';
            continue;
        }
        if(s[i]=='u'){
            if(s[i+1]=='u'){
                ans++;
                s[i+1]='s';
            }
        }
    }
    cout<<ans<<endl;
}
