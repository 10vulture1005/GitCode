void vulture(){
int n;
cin>>n;
vector<int> a(n+1);

for(int i = 2; i <=n; i++) {
    cin>>a[i];
}
string s;
cin>>s;

vector<pair<int,int>> pre(n+1);
vector<bool> v(n+1);

for(int i = 1; i <=n; i++) {
    if(s[i-1]=='W')
    pre[i].first=1;
    else
    pre[i].second=-1;
}


for(int i = n; i >=1; i--) {
    
    if(v[i])continue;
    int j = i;
    v[i]=1;
    pair<int,int> p=pre[i];

    while(j!=0){
        int tm = j;
        j=a[j];
        
    // cout<<tm<<'>'<<j<<endl;
    if(v[j]){
        pre[j].first+=p.first;
        pre[j].second+=p.second;
    }else{
        pre[j].first+=pre[tm].first;
        pre[j].second+=pre[tm].second;
        p = pre[j];
    }
        
        
        v[j]=1;
        

    }
    // cout<<endl;

}
int ans = 0;
for(int i = 1; i <= n; i++) {
    // cout<<i<<' '<<pre[i].first<< ' '<<pre[i].second<<endl;
    if(pre[i].first+pre[i].second==0)ans++;
}
cout<<ans<<endl;
}
