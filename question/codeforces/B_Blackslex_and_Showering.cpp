void vulture(){
    int n;
    cin>>n;
    vector<int> a(n);
    vin(a,a.size());


    int sum = 0;
    for(int i = 0; i < n-1; i++) {
        sum+=abs(a[i]-a[i+1]);
    }
    int ans = min(sum-abs(a[0]-a[1]),sum-abs(a[n-1]-a[n-2]));
    for(int i = 1; i < n-1; i++) {
        ans = min(ans,sum-abs(a[i]-a[i-1])-abs(a[i]-a[i+1])+abs(a[i-1]-a[i+1]));
    }
    cout<<ans<<endl;
}
