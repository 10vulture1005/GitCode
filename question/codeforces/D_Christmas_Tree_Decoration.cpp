int mod = 998244353;
vector<int> fac(51);
vector<int> infac(51);

int ncr(int n, int r) {
if(r < 0 || r > n) return 0LL;
        return fac[n] * infac[r] % mod * infac[n-r] % mod;
}
int modpow(int a, int b){
    int r = 1;
    a %= mod;
    while(b){
        if(b & 1) r = r * a % mod;
        a = a * a % mod;
        b >>= 1;
    }
    return r;
}


void vulture(){
    int n;
    cin>>n;
    vector<int> a(n+1);

    vin(a,a.size());
    int sum = accumulate(a.begin(),a.end(),0LL);
    
    int q = sum/n;
    int r = sum%n;


    int need = 0;
    int rej=0;
    bool f = 1;
    for(int i = 1; i <= n; i++) {
        if(a[i]>q+1){
            f=0;
            break;

        }else if(a[i]==q+1){
            need++;
        }else{
            rej++;
        }
    }

    if(f==0 or need>r or r-need>rej){
        cout<<0<<endl;
        return;
    }
    int ans = ncr(rej,r-need);
    ans = ((ans)%mod*(fac[r])%mod)%mod;
    ans = ((ans%mod)*(fac[n-r])%mod)%mod;
    cout<<ans<<endl;
    
}
