int sum(int n,int k){

    if(n>k){
        int l = n-k;
        int ans = l*(2*(k-1)-(l-1))/2;
        return (k*(k+1)/2)+ans;
    }else{
        return n*(n+1)/2;
    }
    
}
void vulture() {
int k,x;
cin>>k>>x;

int s = 1,e = 2*k-1;


while(s<e){
    int m = s+(e-s)/2;
    if(sum(m,k)<x){
        s=m+1;
    }else{
        e = m;
    }
}
cout<<s<<endl;

}
