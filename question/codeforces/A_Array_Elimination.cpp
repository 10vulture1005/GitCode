vector<int> getFactors(int n)
{
    vector<int> factors;
    for (int i = 1; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            factors.push_back(i);
            if (i != n / i)
            { // avoid duplicate when i == n/i
                factors.push_back(n / i);
            }
        }
    }
    sort(factors.begin(), factors.end()); // optional: sorted order
    return factors;
}

void vulture()
{
    int n;
    cin >> n;
    vector<int> a(n);
    vin(a, a.size());
    vector<int> bit(31, 0);
    for (int x : a)
    {
        for (int b = 0; b < 31; ++b)
        {
            if (x & (1 << b))
                bit[b]++;
        }
    }
    int l = -1;
    for (int i = 31 - 1; i >= 0; i--)
    {
        if (bit[i] != 0)
        {
            l = i + 1;  
            break;
        }
    }

    // for(int i = 0; i <l;i++){
    // cout<<bit[i]<<" ";
    // }
    // cout<<endl;

    int g = 0;
    for (int i = 0; i < l; i++)
    {
        g = gcd(g, bit[i]);
    }

    if (l == -1)
    {
        for (int i = 0; i < n; i++)
        {
            cout << i + 1 << ' ';
        }
        cout << endl;
        return;
    }
    vector<int> ans = getFactors(g);
    for (int i = 0; i < ans.size(); i++)
    {
        cout << ans[i] << " ";
    }
    cout << endl;
}
