void vulture()
{
    int n, k;
    cin >> n >> k;
    if (k % 2 == 1)
    {
        for (int i = 0; i < k; i++)
        {
            cout << n << " ";
        }
        cout << endl;
    }
    else
    {

        int msb = -1, secmsb = -1;
        for (int i = 0; i < 31; i++)
        {
            if ((n >> i) & 1LL)
            {
                secmsb = msb;
                msb = i;
            }
        }

        for (int i = 0; i < k - 2; i++)
        {
            cout << n << " ";
        }

        int x = 0, y = 0;

        if (secmsb == -1)
        {
            x = n;
            y = 0;
        }
        else
        {
            y = (1LL << secmsb);
            x = n ^ y;

            for (int i = 0; i < 31; i++)
            {
                if (!((n >> i) & 1))
                {
                    if ((y | (1 << i)) <= n && (x | (1 << i)) <= n)
                    {
                        y |= (1 << i);
                        x |= (1 << i);
                    }
                }
            }
        }
        cout << x << ' ' << y << endl;
    }
}
